    private void PassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PassActionPerformed
       int userId = User.currentUserId; // ID user yang sedang login
Connection conn = null;
PreparedStatement ps = null;

try {
    conn = koneksi.getKoneksi();

    // Input password baru
    String newPassword = JOptionPane.showInputDialog(this, "Enter new password:");
    if (newPassword == null || newPassword.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Password change canceled.");
        return;
    }

    // Konfirmasi password baru
    String confirmPassword = JOptionPane.showInputDialog(this, "Confirm new password:");
    if (!newPassword.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match!");
        return;
    }

    // Update password di database
    String sql = "UPDATE user SET password = ? WHERE id_user = ?";
    ps = conn.prepareStatement(sql);
    ps.setString(1, newPassword.trim());
    ps.setInt(2, userId);

    int rows = ps.executeUpdate();
    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "Password successfully changed.");
    } else {
        JOptionPane.showMessageDialog(this, "User not found?");
    }

} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error changing password: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
} finally {
    try { if (ps != null) ps.close(); } catch (SQLException ignored) {}
    // koneksi tidak ditutup agar tetap bisa dipakai
}

    }//GEN-LAST:event_PassActionPerformed

