    private void changeVolumeBy(int delta) {
        currentVolume += delta;
        if (currentVolume > 100) {
            currentVolume = 100;
        }
        if (currentVolume < 0) {
            currentVolume = 0;
        }

        // Update volume label (you have jText2 for volume display)
        jText2.setText(currentVolume + "%");

        // Update volume live (optional: you can choose to apply only on Save)
        AudioPlayer.getInstance().setVolume(currentVolume / 100f);
    }

