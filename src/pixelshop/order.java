    private void CheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckActionPerformed
         try {
        Connection conn = koneksi.getKoneksi();
        String selectedProduct = jList2.getSelectedValue();

        String getIdQuery = "SELECT id_nota FROM nota WHERE order_time = ?";
        PreparedStatement getIdStmt = conn.prepareStatement(getIdQuery);
        getIdStmt.setString(1, selectedProduct);
        ResultSet rs = getIdStmt.executeQuery();

        if (rs.next()) {
            int idNota = rs.getInt("id_nota");

            // âœ… Open the detail page and pass the ID
            NotaDetail detailPage = new NotaDetail(idNota);
            detailPage.setVisible(true);
            this.dispose(); // optional: close current window

        } else {
            Bubble.setText("Hm? I don't think I recognize that product.");
        }

    } catch (Exception e) {
        e.printStackTrace();
        Bubble.setText("Error processing request: " + e.getMessage());
    }
    }//GEN-LAST:event_CheckActionPerformed

