 private void loadProducts() {
    try {
        Connection conn = koneksi.getKoneksi();

        String sql = " SELECT DISTINCT n.order_time FROM nota n JOIN transaksi t ON n.id_nota = t.id_nota WHERE t.id_customer = ? ";

        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setInt(1, User.currentUserId); // filter by logged-in user

        ResultSet rs = stmt.executeQuery();

        DefaultListModel<String> productModel = new DefaultListModel<>();
        while (rs.next()) {
            productModel.addElement(rs.getString("order_time"));
        }
        jList2.setModel(productModel);

    } catch (SQLException e) {
        e.printStackTrace();
    }
    System.out.println("Current user id: " + User.currentUserId);

}


    
   





